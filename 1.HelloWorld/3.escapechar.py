# new line (\n)
splitString = "This string has been\nsplit over\nseveral\nlines"
print(splitString)

# tab space (\t)
tabbedString ="1\t2\t3\t4\t5"
print(tabbedString)

# backslash espace single and double quotest ('', "")
print('The pet shop owner said "No, no\'e\'s uh,...he\'s resting".')
# or
print("The pet shop owner said \"No, no, \
 'e's uh...he's resting\".")

print("""The pet shop owner said "No, no, \
 'e's uh,...he's resting". """)

anotherSplitString  ="""This string has been \
split over \
several \
lines"""

print(anotherSplitString)

# using double backslash to escape
print("C:\\Users\\timbulchalka\\notes.txt")
# using raw string to escape
