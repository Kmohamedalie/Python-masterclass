# forwards range with forloop
print("*" * 28)
print("forwards range with forloop")
print("*" * 28)
for i in range(0,10,2):
    print("i is now {}".format(i))

print()


# backwards range with forloop
print("*" * 28)
print("backwards range with forloop")
print("*" * 28)
for i in range(10,0,-2):
    print("i is now {}".format(i))
